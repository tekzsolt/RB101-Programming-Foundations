# what is != and where should you use it?
# It means something it's not equal. Should be used in if/else statements.

# put ! before something, like !user_name
# Turns the object into opposite boolean

# put ! after something, like words.uniq!
# Usually it's a sign of mutating the object (but it's not a guarantee, ! it's part of the method name)

# put ? before something
# No special meaning. ? could be used as a ternary operator

# put ? after something
# Usually it returns a true or false from method however it's not a gurantee, ? it's part of the method name

# put !! before something, like !!user_name
# Returns the object boolean value
